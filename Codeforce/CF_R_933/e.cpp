int op(int a, int b) {
    return min(a, b);
}

int e() {
    return (int)(LLONG_MAX);
}

void solve() {
    int n, m, k, d;
    cin >> n >> m >> k >> d;

    vector<vector<int>> g(n,vector<int>(m));
    for(int i = 0; i < n; i++)
        for(int j = 0; j < m; j++)
            cin >> g[i][j];
        
    vector<int> minCost(n,0);

    for(int i = 0; i < n; i++) {
        vector<int> cost(m,LLONG_MAX);
        cost[0] = 1;
        atcoder::segtree<int, op, e> st(cost);

        for(int j = 1; j < m; j++) {
            cost[j] = st.prod(max(int(0),j-d-1), j) + g[i][j] + 1;
            st.set(j,cost[j]);
        }
        minCost[i] = cost[m-1];
    }


    vector<int>pre(n+1,0);
    for(int i = 1; i <= n; i++) {
        pre[i] = pre[i-1] + minCost[i-1];
    }

    int tot = LLONG_MAX;
    for(int i = 0; i < n; i += 1) {
        if(i+k-1 < n) {
            tot = min(tot, pre[i+k] - pre[i]);
        }
    }
   
    cout << tot;

}
