void solve() {
    long long a, b;
    cin >> a >> b;

    long long l = lcm(a,b);

    if(l != a && l != b) cout << l;
    else {
        long long aa, bb;
        auto spf = [&](long long num)->int {
            

            for(long long i=2;i*i<=num;i++){
                while(num%i==0){
                    // pri.pb(i);
                    // num/=i;
                    return i;
                }
            }  

            return num;     
        };

        aa = spf(a);
        bb = spf(b);

        long long mn = min(aa, bb);
        if(mn == 1) 
            mn = max(aa,bb);
        cout << l * mn;

    }


}
