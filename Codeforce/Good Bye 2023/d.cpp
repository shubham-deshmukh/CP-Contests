void solve() {
    int n;
    cin >> n;

    if(n == 1) {
        cout << 1;
        return ;
    }

    int nn = 3;
    queue<string> q;
    q.push("169");
    q.push("196");
    q.push("961");

    while(nn != n) {
        // Generate n-2 squares by appending zeros
        for(int i = 0; i < nn; i++) {
            string t = q.front(); q.pop();
            q.push(t+"00");
        }

        // Generate remaining 2 squares
        // 1..3 --> 1..6..9 
        // 3..1 --> 1..9..6
        auto getNum = [&](const string s)->string {
            string num = "";
            num.push_back(s[0]);
            for(int i = 0; i < (nn-1) / 2; i++) {
                num.push_back('0');
            }
            num.push_back(s[1]);
            for(int i = 0; i < (nn-1) / 2; i++) {
                num.push_back('0');
            }
            num.push_back(s[2]);    
            return num;        
        };

        q.push(getNum("169"));
        q.push(getNum("961"));
        
        nn += 2;
    }

    while(!q.empty()) {
        cout << q.front();
        q.pop();
        if((int)q.size()) cout << endl;
    }


}
